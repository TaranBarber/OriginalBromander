package bromander;

public class Stat {
	int hp, mana, att, dex, def, smartness;
	
	public Stat(int hp, int mana, int att, int dex, int def, int smartness)
	{
		this.hp = hp;
		this.mana = mana;
		this.att = att;
		this.dex = dex;
		this.def = def;
		this.smartness = smartness;
	}
	
	
}
